package com.penguinz22.SnakeGame.fruits;

public class FruitBuff {

    public final float speedBoost;
    public final float ghosting;
    public final float invincibility;

    public FruitBuff(float speedBoost, float ghosting, float invincibility) {
        this.speedBoost = speedBoost;
        this.ghosting = ghosting;
        this.invincibility = invincibility;
    }

}
